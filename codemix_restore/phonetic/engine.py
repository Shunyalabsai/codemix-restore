    def _init_metaphone(self) -> None:
        """Initialize Double Metaphone index."""
        try:
            from metaphone import doublemetaphone
            self._metaphone_func = doublemetaphone

            for word in self._word_set:
                codes = doublemetaphone(word)
                for code in codes:
                    if code:
                        self._metaphone_index.setdefault(code, []).append(word)

            logger.info("Metaphone index built with %d codes", len(self._metaphone_index))
        except ImportError:
            logger.warning("metaphone package not installed; phonetic matching disabled")

    def _init_symspell(self) -> None:
        """Initialize SymSpell for edit-distance lookup."""
        try:
            from symspellpy import SymSpell
            self._symspell = SymSpell(max_dictionary_edit_distance=self._max_edit_distance)
            for word in self._word_set:
                freq = max(1, len(self._word_set) - self._word_freq.get(word, len(self._word_set)))
                self._symspell.create_dictionary_entry(word, freq)
            logger.info("SymSpell index built with %d words", len(self._word_set))
        except ImportError:
            logger.warning("symspellpy package not installed; edit-distance matching disabled")

