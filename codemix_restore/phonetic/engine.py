from dataclasses import dataclass
from pathlib import Path

logger = logging.getLogger(__name__)


@dataclass
class MatchResult:
    """Result of a phonetic dictionary match."""
    english_word: str
    score: float  # 0.0 to 1.0
    match_type: str  # "exact", "phonetic", "edit_distance"
    frequency_rank: int  # Lower = more common


class PhoneticMatcher:
    """English word lookup using exact match, Double Metaphone, and edit distance.

    Uses a frequency-weighted English dictionary for scoring matches.
    Falls back gracefully if optional dependencies (metaphone, symspellpy) are unavailable.
    """

    def __init__(self, dict_path: str | Path | None = None, max_edit_distance: int = 1):
        self._max_edit_distance = max_edit_distance

        # Core data structures
        self._word_set: set[str] = set()  # Lowercase English words
        self._word_freq: dict[str, int] = {}  # word -> frequency rank (1 = most common)
        self._metaphone_index: dict[str, list[str]] = {}  # metaphone code -> words

        # Optional components (loaded lazily)
        self._symspell = None
        self._metaphone_func = None

        self._load_dictionary(dict_path)
        self._init_metaphone()
        self._init_symspell()

    def _load_dictionary(self, dict_path: str | Path | None) -> None:
        """Load English dictionary with frequency data.

        Priority:
        1. Explicit dict_path if provided
        2. Bundled en_dict_30k.json (CMU Dict + SUBTLEX-US, 30K words)
        3. Hardcoded built-in dictionary (~600 common words)
        """
        if dict_path and os.path.exists(dict_path):
            self._load_from_file(Path(dict_path))
            return

        # Try bundled dictionary (generated by scripts/build_dictionary.py)
        bundled = Path(__file__).parent.parent / "data" / "en_dict_30k.json"
        if bundled.exists():
            self._load_from_file(bundled)
            logger.info("Loaded bundled dictionary from %s", bundled)
            return

        self._load_builtin()

    def _load_from_file(self, path: Path) -> None:
        """Load dictionary from a file. Supports JSON and text formats."""
        if path.suffix == ".json":
            with open(path) as f:
                data = json.load(f)
            for rank, (word, _freq) in enumerate(
                sorted(data.items(), key=lambda x: -x[1]), 1
            ):
                w = word.lower().strip()
                self._word_set.add(w)
                self._word_freq[w] = rank
        else:
            # Text file: one word per line, optionally with frequency
            with open(path) as f:
                for rank, line in enumerate(f, 1):
                    parts = line.strip().split()
                    if parts:
                        w = parts[0].lower()
                        self._word_set.add(w)
                        self._word_freq[w] = rank

