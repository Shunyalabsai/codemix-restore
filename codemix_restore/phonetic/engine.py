    def lookup(self, romanized_word: str, top_k: int = 3) -> list[MatchResult]:
        """Look up a romanized word against the English dictionary.

        Tries in order: transliteration variants, exact match, metaphone match,
        edit-distance match.

        Args:
            romanized_word: The romanized form of an Indic word.
            top_k: Maximum number of results to return.

        Returns:
            List of MatchResult sorted by score (highest first).
        """
        word = romanized_word.lower().strip()
        if not word:
            return []

        results: list[MatchResult] = []

        # 0. Check transliteration variant table (highest priority for known patterns)
        if word in self._TRANSLIT_VARIANTS:
            eng = self._TRANSLIT_VARIANTS[word]
            if eng in self._word_set:
                results.append(MatchResult(
                    english_word=eng,
                    score=0.95,  # High but not 1.0 (not an exact match)
                    match_type="translit_variant",
                    frequency_rank=self._word_freq.get(eng, 999999),
                ))
                return results[:top_k]

        # 1. Exact match
        if word in self._word_set:
            results.append(MatchResult(
                english_word=word,
                score=1.0,
                match_type="exact",
                frequency_rank=self._word_freq.get(word, 999999),
            ))
            return results[:top_k]

        # 2. Metaphone match
        # Skip metaphone entirely for very short romanized inputs — they produce
        # too many false positives (e.g., "am", "an", "he", "ja" match dozens of words)
        if self._metaphone_func and len(word) > 2:
            codes = self._metaphone_func(word)
            seen: set[str] = set()
            for code in codes:
                if code and code in self._metaphone_index:
                    for candidate in self._metaphone_index[code]:
                        if candidate not in seen:
                            seen.add(candidate)
                            freq_rank = self._word_freq.get(candidate, 999999)
                            # Score based on phonetic match + frequency
                            freq_score = 1.0 / (1.0 + freq_rank / 100.0)
                            score = 0.7 + 0.3 * freq_score
                            # Penalize short English words — they match too many
                            # native words phonetically (go, so, do, me, up, in, etc.)
                            if len(candidate) <= 2:
                                score = max(0.2, score - 0.35)
                            elif len(candidate) <= 3:
                                score = max(0.3, score - 0.20)
                            results.append(MatchResult(
                                english_word=candidate,
                                score=score,
                                match_type="phonetic",
                                frequency_rank=freq_rank,
                            ))

        # 3. Edit-distance match
        # Skip edit-distance for very short romanized inputs — too many false positives
        if self._symspell and len(word) > 2:
            from symspellpy import Verbosity
            suggestions = self._symspell.lookup(
                word, Verbosity.CLOSEST, max_edit_distance=self._max_edit_distance
            )
            for sug in suggestions:
                if sug.term not in {r.english_word for r in results}:
                    freq_rank = self._word_freq.get(sug.term, 999999)
                    # Score inversely proportional to edit distance
                    dist_score = 1.0 - sug.distance / max(len(word), 1)
                    freq_score = 1.0 / (1.0 + freq_rank / 100.0)
                    score = 0.4 * dist_score + 0.3 * freq_score + 0.3 * (1 if sug.distance <= 1 else 0)
                    # Penalize short English words — they match too many
                    # native words via edit distance
                    if len(sug.term) <= 2:
                        score = max(0.2, score - 0.35)
                    elif len(sug.term) <= 3:
                        score = max(0.3, score - 0.20)
                    results.append(MatchResult(
                        english_word=sug.term,
                        score=score,
                        match_type="edit_distance",
                        frequency_rank=freq_rank,
                    ))

        # Sort by score descending
        results.sort(key=lambda r: (-r.score, r.frequency_rank))
        return results[:top_k]

    def is_english(self, romanized_word: str, threshold: float = 0.6) -> tuple[bool, MatchResult | None]:
        """Check if a romanized word is likely English.

        Args:
            romanized_word: Romanized form of the word.
            threshold: Minimum score to consider a match.

        Returns:
            Tuple of (is_english, best_match_or_none).
        """
        matches = self.lookup(romanized_word, top_k=1)
        if matches and matches[0].score >= threshold:
            return True, matches[0]
        return False, None

    @property
    def vocab_size(self) -> int:
        return len(self._word_set)
